/* ---- helpers ---- */

static void put(image_t *img, int x, int y, uint32_t color)
{
    if (x < 0 || x >= img->w || y < 0 || y >= img->h) return;
    img->px[y * img->w + x] = color;
}

static void hline(image_t *img, int x0, int x1, int y, uint32_t color)
{
    for (int x = x0; x <= x1; x++) put(img, x, y, color);
}

static void vline(image_t *img, int x, int y0, int y1, uint32_t color)
{
    for (int y = y0; y <= y1; y++) put(img, x, y, color);
}

static void draw_text(image_t *img, int x, int y, const char *str, uint32_t color)
{
    for (; *str; str++) {
        uint8_t c = (uint8_t)*str;
        if (c < 0x20 || c > 0x7E) { x += 6; continue; }
        const uint8_t *g = font5x7[c - 0x20];
        for (int col = 0; col < 5; col++) {
            for (int row = 0; row < 7; row++) {
                if (g[col] & (1 << row))
                    put(img, x + col, y + row, color);
            }
        }
        x += 6;
    }
}

/* Bresenham line */
static void line_segment(image_t *img, int x0, int y0, int x1, int y1, uint32_t color)
{
    int dx = abs(x1-x0), sx = x0 < x1 ? 1 : -1;
    int dy = abs(y1-y0), sy = y0 < y1 ? 1 : -1;
    int err = (dx > dy ? dx : -dy) / 2;
    for (;;) {
        put(img, x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = err;
        if (e2 > -dx) { err -= dy; x0 += sx; }
        if (e2 <  dy) { err += dx; y0 += sy; }
    }
}

/* ---- public ---- */

image_t *image_new(int w, int h)
{
    image_t *img = malloc(sizeof(*img));
    if (!img) return NULL;
    img->px = calloc((size_t)w * h, sizeof(uint32_t));
    if (!img->px) { free(img); return NULL; }
    img->w = w; img->h = h;
    return img;
}

void image_free(image_t *img)
{
    if (img) { free(img->px); free(img); }
}

#define PAD_LEFT  48
#define PAD_RIGHT 12
#define PAD_TOP   20
#define PAD_BOT   16

void chart_draw(image_t *img, const double *values, int n,
                double y_min, double y_max,
                uint32_t color, const char *title)
{
    uint32_t BG       = RGB(0x0f,0x11,0x17);
    uint32_t GRID     = RGB(0x2a,0x2d,0x3a);
    uint32_t AXIS     = RGB(0x44,0x48,0x58);
    uint32_t TEXT_COL = RGB(0x88,0x92,0xa4);

    /* fill background */
    for (int i = 0; i < img->w * img->h; i++)
        img->px[i] = BG;

    int cx0 = PAD_LEFT, cx1 = img->w - PAD_RIGHT;
    int cy0 = PAD_TOP,  cy1 = img->h - PAD_BOT;
    int cw = cx1 - cx0, ch = cy1 - cy0;

    /* title */
    draw_text(img, cx0, 4, title, TEXT_COL);

    /* horizontal grid lines + y-axis labels (5 divisions) */
    double range = y_max - y_min;
    if (range == 0) range = 1;
    for (int i = 0; i <= 4; i++) {
        int y = cy1 - (int)(i * ch / 4);
        hline(img, cx0, cx1, y, GRID);
        double val = y_min + range * i / 4.0;
        char label[16];
        snprintf(label, sizeof(label), "%6.1f", val);
        draw_text(img, 0, y - 3, label, TEXT_COL);
    }

    /* axes */
    vline(img, cx0, cy0, cy1, AXIS);
    hline(img, cx0, cx1, cy1, AXIS);

    if (n < 2) return;

    /* plot line */
    int prev_x = -1, prev_y = -1;
    for (int i = 0; i < n; i++) {
        if (isnan(values[i])) { prev_x = -1; continue; }
        int x = cx0 + (int)((double)i / (n - 1) * cw);
        int y = cy1 - (int)((values[i] - y_min) / range * ch);
        if (y < cy0) y = cy0;
        if (y > cy1) y = cy1;
        if (prev_x >= 0)
            line_segment(img, prev_x, prev_y, x, y, color);
        prev_x = x; prev_y = y;
    }
}
