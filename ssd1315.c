/* Degree symbol (U+00B0): small superscript circle */
static const uint8_t glyph_degree[5] = {0x06,0x09,0x09,0x06,0x00};

/* ---- display handle ---- */

struct ssd1315 {
    int     fd;
    uint8_t fb[SSD1315_PAGES][SSD1315_WIDTH]; /* 1 byte = 8 vertical pixels */
};

/* ---- internal helpers ---- */

static int oled_cmd(ssd1315_t *d, uint8_t cmd)
{
    if (ioctl(d->fd, I2C_SLAVE, SSD1315_ADDR) < 0) {
        fprintf(stderr, "oled ioctl: %s\n", strerror(errno));
        return -1;
    }
    uint8_t buf[2] = { CTRL_CMD_SINGLE, cmd };
    if (write(d->fd, buf, 2) != 2) {
        fprintf(stderr, "oled cmd 0x%02x: %s\n", cmd, strerror(errno));
        return -1;
    }
    return 0;
}

static void oled_init_sequence(ssd1315_t *d)
{
    static const uint8_t cmds[] = {
        0xAE,       /* display off */
        0xD5, 0x80, /* clock divide / osc freq */
        0xA8, 0x3F, /* mux ratio 64 */
        0xD3, 0x00, /* display offset 0 */
        0x40,       /* start line 0 */
        0x8D, 0x14, /* charge pump on */
        0x20, 0x00, /* horizontal addressing mode */
        0xA1,       /* segment remap (col 127 = SEG0) */
        0xC8,       /* COM scan direction remapped */
        0xDA, 0x12, /* COM pins hardware config */
        0x81, 0xCF, /* contrast */
        0xD9, 0xF1, /* pre-charge period */
        0xDB, 0x40, /* VCOMH deselect level */
        0xA4,       /* display from RAM */
        0xA6,       /* normal (non-inverted) */
        0xAF,       /* display on */
    };
    for (size_t i = 0; i < sizeof(cmds); i++)
        oled_cmd(d, cmds[i]);
}

/* ---- public API ---- */

ssd1315_t *ssd1315_init(int i2c_fd)
{
    ssd1315_t *d = calloc(1, sizeof(*d));
    if (!d) return NULL;
    d->fd = i2c_fd;
    oled_init_sequence(d);
    return d;
}

void ssd1315_close(ssd1315_t *d)
{
    free(d);
}

void ssd1315_clear(ssd1315_t *d)
{
    memset(d->fb, 0, sizeof(d->fb));
}

void ssd1315_flush(ssd1315_t *d)
{
    if (ioctl(d->fd, I2C_SLAVE, SSD1315_ADDR) < 0) return;

    /* Set column and page address window covering entire display */
    uint8_t setup[] = {
        CTRL_CMD_STREAM,
        0x21, 0x00, 0x7F, /* column 0–127 */
        0x22, 0x00, 0x07, /* page   0–7   */
    };
    write(d->fd, setup, sizeof(setup));

    /* Stream all 128×8 = 1024 bytes of framebuffer in one write.
     * +1 byte for the data-stream control byte prefix. */
    uint8_t buf[1 + SSD1315_WIDTH * SSD1315_PAGES];
    buf[0] = CTRL_DATA_STREAM;
    for (int p = 0; p < SSD1315_PAGES; p++)
        memcpy(&buf[1 + p * SSD1315_WIDTH], d->fb[p], SSD1315_WIDTH);
    write(d->fd, buf, sizeof(buf));
}

void ssd1315_text(ssd1315_t *d, int page, int col, const char *str)
{
    for (; *str && col < SSD1315_WIDTH; str++) {
        uint8_t c = (uint8_t)*str;
        const uint8_t *glyph;

        /* Detect UTF-8 encoding of U+00B0 DEGREE SIGN (0xC2 0xB0) */
        if (c == 0xC2 && (uint8_t)str[1] == 0xB0) {
            glyph = glyph_degree;
            str++;
        } else if (c >= 0x20 && c <= 0x7E) {
            glyph = font5x7[c - 0x20];
        } else {
            glyph = font5x7[0]; /* fallback: space */
        }

        for (int i = 0; i < 5 && col < SSD1315_WIDTH; i++, col++)
            d->fb[page][col] = glyph[i];
        if (col < SSD1315_WIDTH)
            d->fb[page][col++] = 0x00; /* 1 px spacing */
    }
}
